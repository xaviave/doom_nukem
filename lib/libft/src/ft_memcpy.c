#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	void	*dest_;

	dest_ = dest;
	while (n-- > 0)
		*(unsigned char *)dest++ = *(unsigned char *)src++;
	return (dest_);
}
